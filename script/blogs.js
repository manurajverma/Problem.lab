var area = document.querySelector('#blogpage')
var len = document.title =='Home'?4:blogs.length

for (let i=0; i<len; i++){
    let main = document.createElement('div')
    main.setAttribute('class', 'box')
    area.appendChild(main)

    main.setAttribute('onclick',`showDetail(${blogs[i].id})`)

    let s1 = document.createElement('section')
    let img = document.createElement('img')
    img.setAttribute('src', blogs[i].image)
    s1.appendChild(img)
    main.appendChild(s1)

    let s2 = document.createElement('section')
    let name = document.createTextNode(blogs[i].name)
    s2.appendChild(name)
    main.appendChild(s2)

    let s3 = document.createElement('section')
    let psbdt = document.createTextNode(blogs[i].publishdate)
    s3.appendChild(psbdt)
    main.appendChild(s3)

    let s4 = document.createElement('section')
    let desc = document.createTextNode(blogs[i].description)
    s4.appendChild(desc)
    main.appendChild(s4)
}

function showDetail(id){
    var container = document.getElementById('lbox')
    document.getElementById('lightbox').style.display = "block"
    try{
        document.getElementById('contentarea').remove();
    } catch(e){

    }
    var conarea = document.createElement('div')
    conarea.setAttribute('id','contentarea')
    container.appendChild(conarea)
    

    let main = document.createElement('div')
    main.setAttribute('class', 'box')
    conarea.appendChild(main)

    let s1 = document.createElement('section')
    let img = document.createElement('img')
    img.setAttribute('src', blogs[id-1].image)
    s1.appendChild(img)
    main.appendChild(s1)

    let s2 = document.createElement('section')
    let name = document.createTextNode(blogs[id-1].name)
    s2.appendChild(name)
    main.appendChild(s2)

    let s3 = document.createElement('section')
    let psbdt = document.createTextNode(blogs[id-1].publishdate)
    s3.appendChild(psbdt)
    main.appendChild(s3)

    let s4 = document.createElement('section')
    let desc = document.createTextNode(blogs[id-1].description)
    s4.appendChild(desc)
    main.appendChild(s4)
}
